// Animation utility functions
export const staggerChildren = (delay = 100) => ({
  hidden: { opacity: 0, y: 20 },
  visible: (i) => ({
    opacity: 1,
    y: 0,
    transition: {
      delay: i * delay,
      duration: 0.6,
      ease: 'easeOut'
    }
  })
})

